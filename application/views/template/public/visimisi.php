 <span style="color:#d35400; margin-bottom: 30px"><sapn style="color: #2c3e50">BY</sapn> BIRO HUKUM KAB KUDUS</sapn></span><hr>
<h4>VISI:</h4>
<p>Mewujudkan Peraturan Perundang-undangan Daerah yang memberi kepastian dan perlindungan hukum berdasarkan azas keadilan dan kebenaran serta peningkatan kesadaran  Hukum di Masyarakat</p>
<h4>MISI:</h4>
<p>Untuk mewujudkan visi tersebut, Biro Hukum Setda Provinsi DKI Jakarta menerapkan misi sebagai berikut :
  <ol>
    <li> Menyusun peraturan perundang-undangan daerah yang sesuai dengan aspirasi masyarakat dan kebutuhan pembangunan serta pembentukan peraturan perundang-undangan;</li>
    <li>Memberikan bantuan hukum, perlindungan hukum dan kepastian hukum yang berlandaskan azas keadilan dan kebenaran dalam penanganan perkara atau kasus hukum terkait Pemerintah Provinsi DKI Jakarta serta Perlindungan HAM</li>
    <li>Melakukan kajian dan evaluasi terhadap peraturan perundang-undangan daerah sesuai kaidah-kaidah harmonisasi hukum</li>
    <li>Memberikan pelayanan informasi dan dokumentasi hukum yang cepat, tepat, dan akurat;</li>
    <li>Membentuk sumber daya aparatur bidang hukum dan peraturan perundang-undangan yang profesional</li>
    <li>Meningkatkan pemahaman dan kesadaran hukum aparatur dan masyarakat terhadap peraturan perundang-undangan;</li>
    <li>Melaksanakan pembinaan pelaksanaan peraturan perundang-undangan</li>
 </ol>
</p>